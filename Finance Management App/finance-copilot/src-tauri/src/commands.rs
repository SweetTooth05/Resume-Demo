// ── Chat / SLM ───────────────────────────────────────────────────────────────

#[tauri::command]
pub async fn send_chat_message(
    state: State<'_, AppState>,
    app: tauri::AppHandle,
    message: String,
    message_id: String,
    history: Vec<Value>,
) -> Result<(), String> {
    let system_prompt = build_system_prompt(&state).await;

    let mut messages = vec![serde_json::json!({
        "role": "system",
        "content": system_prompt,
    })];
    messages.extend(history);
    messages.push(serde_json::json!({
        "role": "user",
        "content": message,
    }));

    inference::stream_chat(
        state.inference.clone(),
        app,
        message_id,
        messages,
    );

    Ok(())
}

async fn build_system_prompt(state: &AppState) -> String {
    let conn = db::get_connection(&state.db_path).ok();
    let mut context_parts = vec![
        "You are a local personal finance assistant. All data stays on this device.".to_string(),
        "Back every numeric claim with an MCP tool call — never compute math yourself.".to_string(),
        "Available tools: query_transactions, aggregate, compare_periods, forecast, calculate, tax_estimate, plot.".to_string(),
    ];

    // Inject latest cashflow summary if DB available
    if let Some(conn) = conn {
        if let Ok(cashflow) = db::get_monthly_cashflow(&conn, 1) {
            if let Some((month, income, expenses)) = cashflow.last() {
                context_parts.push(format!(
                    "Recent context: {} — income ${:.0}, expenses ${:.0}, net ${:.0}.",
                    month, income, expenses, income - expenses
                ));
            }
        }
    }

    context_parts.join("\n")
}

