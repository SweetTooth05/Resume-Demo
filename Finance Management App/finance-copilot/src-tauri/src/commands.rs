// ── Hardware Detection ────────────────────────────────────────────────────────

#[tauri::command]
pub async fn detect_hardware() -> Result<HardwareProfile, String> {
    Ok(probe_hardware())
}

pub fn probe_hardware() -> HardwareProfile {
    use sysinfo::System;
    let mut sys = System::new();
    sys.refresh_memory();
    let ram_gb = sys.total_memory() / 1_073_741_824;

    // Try NVIDIA first via nvidia-smi
    if let Some((gpu_name, vram_gb)) = detect_nvidia() {
        let (backend, model_tier) = if vram_gb.unwrap_or(0.0) >= 6.0 {
            ("cuda".to_string(), "7B".to_string())
        } else {
            ("cuda".to_string(), "3B".to_string())
        };
        return HardwareProfile { backend, model_tier, gpu_name: Some(gpu_name), vram_gb, ram_gb };
    }

    // Try AMD / Vulkan via vulkaninfo
    if let Some(gpu_name) = detect_vulkan_gpu() {
        return HardwareProfile {
            backend: "vulkan".to_string(),
            model_tier: if ram_gb >= 16 { "7B".to_string() } else { "3B".to_string() },
            gpu_name: Some(gpu_name),
            vram_gb: None,
            ram_gb,
        };
    }

    // CPU fallback
    let model_tier = if ram_gb >= 12 { "7B" } else { "3B" }.to_string();
    HardwareProfile { backend: "cpu".to_string(), model_tier, gpu_name: None, vram_gb: None, ram_gb }
}

fn detect_nvidia() -> Option<(String, Option<f64>)> {
    let output = std::process::Command::new("nvidia-smi")
        .args(["--query-gpu=name,memory.total", "--format=csv,noheader,nounits"])
        .output()
        .ok()?;
    if !output.status.success() {
        return None;
    }
    let text = String::from_utf8_lossy(&output.stdout);
    let line = text.lines().next()?;
    let parts: Vec<&str> = line.splitn(2, ',').map(str::trim).collect();
    let name = parts.first()?.to_string();
    let vram_gb = parts.get(1).and_then(|s| s.parse::<f64>().ok()).map(|mb| mb / 1024.0);
    Some((name, vram_gb))
}

fn detect_vulkan_gpu() -> Option<String> {
    // Check for AMD on Linux via DRM
    #[cfg(target_os = "linux")]
    if std::path::Path::new("/dev/dri").exists() {
        if let Ok(entries) = std::fs::read_dir("/sys/bus/pci/devices") {
            for entry in entries.flatten() {
                let vendor_path = entry.path().join("vendor");
                if let Ok(vendor) = std::fs::read_to_string(&vendor_path) {
                    if vendor.trim() == "0x1002" {
                        // AMD GPU
                        let device_path = entry.path().join("device");
                        let device_id = std::fs::read_to_string(&device_path)
                            .unwrap_or_default();
                        return Some(format!("AMD GPU (device {})", device_id.trim()));
                    }
                }
            }
        }
    }
    None
}

// ── API Keys (OS credential vault) ───────────────────────────────────────────

#[tauri::command]
pub async fn save_api_keys(basiq_key: String, eodhd_key: String) -> Result<(), String> {
    store_credential("basiq", &basiq_key).map_err(|e| e.to_string())?;
    store_credential("eodhd", &eodhd_key).map_err(|e| e.to_string())?;
    Ok(())
}

#[tauri::command]
pub async fn get_api_keys() -> Result<Value, String> {
    let basiq = retrieve_credential("basiq").unwrap_or_default();
    let eodhd = retrieve_credential("eodhd").unwrap_or_default();
    Ok(serde_json::json!({
        "basiqApiKey": basiq,
        "eodhdApiKey": eodhd,
    }))
}

fn store_credential(service: &str, secret: &str) -> anyhow::Result<()> {
    if secret.is_empty() {
        return Ok(());
    }
    let entry = keyring::Entry::new("finance-copilot", service)?;
    entry.set_password(secret)?;
    Ok(())
}

fn retrieve_credential(service: &str) -> Option<String> {
    let entry = keyring::Entry::new("finance-copilot", service).ok()?;
    match entry.get_password() {
        Ok(p) => Some(p),
        Err(keyring::Error::NoEntry) => None,
        Err(e) => {
            log::warn!("keyring retrieval failed for {}: {}", service, e);
            None
        }
    }
}

// ── Data Wipe ─────────────────────────────────────────────────────────────────

#[tauri::command]
pub async fn wipe_all_data(state: State<'_, AppState>) -> Result<(), String> {
    log::warn!("Wiping all local data");
    let conn = db::get_connection(&state.db_path).map_err(|e| e.to_string())?;
    db::wipe_all_data(&conn).map_err(|e| e.to_string())?;

    // Clear keyring entries
    for service in &["basiq", "eodhd"] {
        if let Ok(entry) = keyring::Entry::new("finance-copilot", service) {
            let _ = entry.delete_password();
        }
    }

    Ok(())
}
